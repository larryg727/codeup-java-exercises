public class stringExercises {
    public static void main(String[] args) {
        String education = "We don't need no education \nWe don't need no thought control";
        System.out.println(education);

        String quote = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quote);

        String slash = "In windows, the main drive is usually C:\\\\";
        System.out.println(slash);

        String tripSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(tripSlash);
    }
}
